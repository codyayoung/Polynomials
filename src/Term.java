/**
 * Represents a polynomial term as an object.
 * Created by Cody on 4/20/2017.
 */
public class Term {
    //Instance variables
    private int coeff;
    private int exp;

    /**
     * Constructor method for term objects - coefficient and exponent.
     */
    public Term (int c, int e) {
        coeff = c;
        exp = e;
    }
}
